package com.cometproject.server.game.wired.misc;

public class WiredSquare {
    private int x;
    private int y;

    public WiredSquare(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }
}
